import Head from 'next/head';
import React from 'react';
import { GetServerSideProps } from 'next';
import { useDispatch, useSelector } from 'react-redux';

import { Header, Lists, TopNavigation } from '../components';
import { IListPattern } from '../interfaces';
import { setPatternsItems } from '../store/slices/patternsSlice';
import { RootState } from '../store/store';

interface IHome {
  data: IListPattern[];
}

const Home: React.FC<IHome> = ({ data }) => {
  const { items, searchQuery } = useSelector((state: RootState) => state.patterns);
  const dispatch = useDispatch();

  React.useEffect(() => {
    if (searchQuery !== '') {
      const newData = data.filter(
        (item) =>
          item.title.en.toLowerCase().indexOf(searchQuery.toLowerCase()) >= 0 ||
          item.title.ru.toLowerCase().indexOf(searchQuery.toLowerCase()) >= 0,
      );
      dispatch(setPatternsItems(newData));
    } else {
      dispatch(setPatternsItems(data));
    }
  }, [dispatch, data, searchQuery]);

  return (
    <div className="container">
      <Head>
        <title>Next RGXP</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <TopNavigation />
      <Lists data={items} />
    </div>
  );
};

export default Home;

export const getServerSideProps: GetServerSideProps = async (ctx) => {
  const dataJSON = await fetch('https://60feed9f257411001707883e.mockapi.io/all');
  const data: IListPattern[] = await dataJSON.json();

  return {
    props: {
      data,
    }, // will be passed to the page component as props
  };
};
